
int a = 0; // 被除数
int b = 0; // 除数
int n = 0; // 小数点后需要显示的位数
cin >> a >> b >> n; // 从用户输入读取被除数、除数和小数点后的位数

cout << a / b << "."; // 输出整数部分和小数点

int t = a % b; // 计算被除数除以除数的余数
for (int i = 1; i <= n; i++) { // 循环n次以计算小数点后的每一位
    t = t * 10; // 将余数乘以10，以便计算下一位小数
    cout << t / b; // 输出当前位的小数
    t = t % b; // 更新余数，用于下一次循环
}
